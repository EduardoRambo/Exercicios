public class Animal {

    //Attributes 
    protected String animalName;
    protected int age;
    
    
    //Getters
    public String getAnimalName() {
        return this.animalName;
    }
    
    public int getAge() {
        return this.age;
    }
    
    
    //Setters
    
    public void setAnimalName(String animalName) {
        this.animalName = animalName;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
}